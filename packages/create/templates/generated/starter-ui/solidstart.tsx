import { useAgent } from "@better-agent/client/solid";
import { For, Show, createSignal } from "solid-js";
import { client } from "../better-agent/client";

export default function ChatPage() {
    const [input, setInput] = createSignal("");
    const agent = useAgent(client, {
        agent: "__PROVIDER__",
        conversationId: "main",
        hydrateFromServer: true,
        resume: true,
    });

    const send = async (event: SubmitEvent) => {
        event.preventDefault();
        if (agent.status() === "submitted" || agent.status() === "streaming") {
            agent.stop();
            return;
        }
        const value = input().trim();
        if (!value) return;
        setInput("");
        await agent.sendMessage(value);
    };

    return (
        <div class="min-h-screen bg-[color:var(--background)] px-4 py-6 text-[color:var(--foreground)] sm:px-6 sm:py-8">
            <section class="mx-auto grid max-w-3xl gap-4">
                <header class="grid gap-3">
                    <p class="m-0 font-mono text-[11px] uppercase tracking-[0.08em] text-[color:var(--muted)]">
                        Generated by Better Agent
                    </p>
                    <h1 class="m-0 text-[1.85rem] font-semibold tracking-[-0.05em] text-white sm:text-[2.2rem]">
                        __FRAMEWORK_TITLE__
                    </h1>
                    <div class="flex items-center gap-2 border border-[color:var(--border)] bg-[color:var(--panel)] px-3 py-2 font-mono text-[11px] uppercase tracking-[0.2em] text-[color:var(--muted)]">
                        <span class="text-white/55">&gt;_</span>
                        <span class="text-white/78">/agents/__PROVIDER__</span>
                    </div>
                </header>
                <section class="grid gap-3 border border-[color:var(--border)] bg-[color:var(--panel)] p-3 sm:p-4">
                    <div class="flex items-center justify-between gap-3 font-mono text-[11px] uppercase tracking-[0.22em] text-[color:var(--muted)]">
                        <span>conversation main</span>
                        <span>{agent.status()}</span>
                    </div>
                    <section
                        class="grid min-h-[320px] gap-3 border border-[color:var(--border)] bg-[color:var(--panel-muted)] p-3"
                        aria-live="polite"
                    >
                        <Show
                            when={agent.messages().length > 0}
                            fallback={
                                <div class="grid place-items-center border border-dashed border-[color:var(--border)] px-4 py-12 text-center text-sm leading-6 text-[color:var(--muted)]">
                                    No messages yet.
                                </div>
                            }
                        >
                            <div class="grid content-start gap-3 self-start">
                                <For each={agent.messages()}>
                                    {(message) => (
                                        <article
                                            class={`max-w-[88%] border px-2.5 py-2 text-sm leading-[1.55] ${
                                                message.role === "user"
                                                    ? "ml-auto border-[color:var(--border-strong)] bg-white text-black"
                                                    : "border-[color:var(--border)] bg-black text-[color:var(--foreground)]"
                                            }`}
                                        >
                                            <p
                                                class={`m-0 font-mono text-[9px] uppercase tracking-[0.18em] ${
                                                    message.role === "user"
                                                        ? "text-black/55"
                                                        : "text-[color:var(--muted)]"
                                                }`}
                                            >
                                                {message.role}
                                            </p>
                                            <p class="m-0 mt-1.5 whitespace-pre-wrap">
                                                {message.parts
                                                    .filter((part) => part.type === "text")
                                                    .map((part) => part.text)
                                                    .join("\n") || "No text content"}
                                            </p>
                                        </article>
                                    )}
                                </For>
                            </div>
                        </Show>
                    </section>
                    <form class="grid gap-3" onSubmit={(event) => void send(event)}>
                        <textarea
                            value={input()}
                            onInput={(event) => setInput(event.currentTarget.value)}
                            placeholder="Ask something..."
                            aria-label="Message"
                            class="min-h-[96px] w-full resize-none border border-[color:var(--border)] bg-black px-3 py-2.5 text-sm leading-6 text-[color:var(--foreground)] outline-none transition placeholder:text-[color:var(--muted)] focus:border-[color:var(--border-strong)]"
                        />
                        <div class="flex flex-wrap items-center justify-end gap-3">
                            <div class="flex items-center gap-2">
                                <button
                                    type="button"
                                    onClick={() => agent.stop()}
                                    disabled={
                                        agent.status() !== "submitted" &&
                                        agent.status() !== "streaming"
                                    }
                                    class="border border-[color:var(--border)] px-3 py-2 text-sm text-[color:var(--foreground)] transition hover:border-[color:var(--border-strong)] disabled:cursor-not-allowed disabled:opacity-40"
                                >
                                    Stop
                                </button>
                                <button
                                    type="submit"
                                    disabled={
                                        agent.status() === "submitted" ||
                                        agent.status() === "streaming" ||
                                        input().trim().length === 0
                                    }
                                    class="border border-white bg-white px-3 py-2 text-sm font-medium text-black transition hover:bg-white/90 disabled:cursor-not-allowed disabled:opacity-40"
                                >
                                    {agent.status() === "streaming" ||
                                    agent.status() === "submitted"
                                        ? "Streaming"
                                        : "Send"}
                                </button>
                            </div>
                        </div>
                        <Show when={agent.error()}>
                            {(error) => (
                                <p class="m-0 border border-red-500/40 bg-red-500/10 px-3 py-2 text-sm text-red-200">
                                    {error().message}
                                </p>
                            )}
                        </Show>
                        <div class="border-t border-[color:var(--border)] pt-3">
                            <p class="m-0 font-mono text-[10px] uppercase tracking-[0.18em] text-[color:var(--muted)]">
                                Docs{" "}
                                <a
                                    href="https://www.better-agent.com/docs"
                                    target="_blank"
                                    rel="noreferrer"
                                    class="font-medium text-white/88"
                                >
                                    better-agent.com/docs
                                </a>
                            </p>
                        </div>
                    </form>
                </section>
            </section>
        </div>
    );
}
